package com.ulflander.app.model;

/**
 * Lists directions for consolidation of token scores.
 *
 * Created by Ulflander on 4/24/14.
 */
public enum TokenConsolidationDirection {

    /**
     * Consolidate backward.
     */
    BACKWARD,

    /**
     * Consolidate forward.
     */
    FORWARD,

    /**
     * Consolidate both backward and forward.
     */
    BOTH;

}
